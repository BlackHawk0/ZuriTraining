// Assignment on loops

class Loops{ 
public static void main(String[] args) {
    System.out.println("Even Numbers between 1 and 20 :");
    
    //declaring the range
    for (int j=1; j<=20; j++ ){

        // if j divided by 2 is 0 then the number is even.
        if(j%2==0){
            System.out.println(j);
        }
    }
}
}